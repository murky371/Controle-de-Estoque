    private void buttonRelatorioDeEstoqueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRelatorioDeEstoqueActionPerformed
        // TODO add your handling code here:
        try{
        Date data = new Date();
        DateFormat df = new SimpleDateFormat("DD__MM__YYYY");
        RelatorioDeEstoque relatorioEstoque = new RelatorioDeEstoque("Documentos" + "Relatorio_de_Estoque" + df.format(data)+".pdf");
        if (relatorioEstoque.geraRelatorio()){
            JOptionPane.showMessageDialog(this, "Relatorio gerado com sucesso! O relatorio" +" gerado se encontra em Documentos", "Sucesso",JOptionPane.OK_OPTION);
            
        } else{
            JOptionPane.showMessageDialog(this,"Relatorio nao gerado!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
     }  catch(Exception e){
         Logger.getLogger(PrincipalView.class.getName()).log(Level.SEVERE, null, e);
     }
    }//GEN-LAST:event_buttonRelatorioDeEstoqueActionPerformed
    
